#include "libft.h"

int	ft_isupper(int c)
{
	if (c >= 'a' && c <= 'z')
		return (1);
	return (0);
}

int	ft_toupper(int c)
{
	if (ft_isupper(c))
		c -= 32;
	return (c);
}
